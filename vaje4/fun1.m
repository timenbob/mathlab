function vk = fun1(tk)

n=10000;
ro = @(y)  5.038514626813336e-03 + 6.488352761211669e-02*((y-40000)/40000).^(2) + 1.158827281393931e+00*((y-40000)/40000).^(4);
g = @(y) 9.81*(6371000/(6371000+y))^2;
m = 105;
c = 1;
S = 1.2;
K = @(y) 1/2*ro(y)*c*S;

zac=[40000,0];

dY = @(t,Y) [Y(2); -g(Y(1))-K(Y(1))/m*abs(Y(2)).*Y(2)];


t = linspace(0,tk,n);
[~,Yres] = ode45(dY,t,zac);


vk = Yres(end,2);


end
